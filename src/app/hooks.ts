import { TypedUseSelectorHook, useDispatch, useSelector } from "react-redux";
import type { RootState, AppDispatch } from "./store";

/**
 * ТИПИЗИРОВАННЫЕ REDUX ХУКИ ДЛЯ WARHAMMER 40,000 FANDOM WIKI
 * 
 * Создает типизированные версии стандартных React-Redux хуков для обеспечения
 * типобезопасности при работе с состоянием приложения и диспетчеризацией действий.
 * 
 * Преимущества:
 * - Исключает необходимость ручной типизации в каждом компоненте
 * - Обеспечивает автодополнение и проверку типов в IDE
 * - Предотвращает runtime ошибки за счет compile-time проверок
 */

/**
 * ТИПИЗИРОВАННЫЙ ХУК ДЛЯ ДИСПЕТЧЕРИЗАЦИИ ДЕЙСТВИЙ
 * 
 * Возвращает функцию dispatch, типизированную в соответствии с настройками store:
 * - Знает все возможные действия (actions) приложения
 * - Проверяет корректность payload передаваемых действий
 * - Учитывает middleware (например, thunk для асинхронных операций)
 * 
 * @returns {AppDispatch} Типизированная функция dispatch
 */
export const useAppDispatch = () => useDispatch<AppDispatch>();

/**
 * ТИПИЗИРОВАННЫЙ ХУК ДЛЯ ДОСТУПА К СОСТОЯНИЮ
 * 
 * Предоставляет типобезопасный доступ к глобальному состоянию приложения:
 * - Знает полную структуру состояния (RootState)
 * - Обеспечивает автодополнение путей к состоянию
 * - Проверяет типы извлекаемых данных
 * 
 * @type {TypedUseSelectorHook<RootState>}
 */
export const useAppSelector: TypedUseSelectorHook<RootState> = useSelector;






