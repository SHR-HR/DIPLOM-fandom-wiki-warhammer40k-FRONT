import { Navigate } from "react-router-dom";

/**
 * КОМПОНЕНТ ПЕРЕНАПРАВЛЕНИЯ НА СТРАНИЦУ 404 ОШИБКИ
 * 
 * Простой компонент-редирект, который выполняет автоматическое перенаправление
 * на универсальную страницу ошибок с кодом 404 в Warhammer 40,000 Fandom Wiki.
 * 
 * НАЗНАЧЕНИЕ:
 * - Используется для обработки некорректных маршрутов в React Router
 * - Обеспечивает централизованное отображение ошибки 404 через ErrorPage
 * - Заменяет текущий маршрут в истории браузера (replace: true)
 * 
 * ПРЕИМУЩЕСТВА ПОДХОДА:
 * - Единообразие: все 404 ошибки показываются через один компонент ErrorPage
 * - Поддержка: использует все возможности основной страницы ошибок (дизайн, действия)
 * - Масштабируемость: легко добавлять дополнительные редиректы для других кодов ошибок
 */
export default function NotFoundPage() {
  /**
   * ВЫПОЛНЕНИЕ ПЕРЕНАПРАВЛЕНИЯ НА СТРАНИЦУ 404 ОШИБКИ
   * 
   * Компонент Navigate из React Router DOM:
   * - to="/error/404" - целевой маршрут с параметром кода ошибки
   * - replace={true} - заменяет текущую запись в истории браузера
   * 
   * Это предотвращает возможность вернуться к некорректному URL через кнопку "Назад"
   */
  return <Navigate to="/error/404" replace />;
}




