import React from "react";

/**
 * ТИПЫ КЛЮЧЕЙ СОРТИРОВКИ ДЛЯ КОМПОНЕНТА ВЫБОРА СОРТИРОВКИ
 * 
 * Определяет все возможные варианты сортировки статей:
 * - По дате: newest (сначала новые), oldest (сначала старые)
 * - По заголовку: title_asc (A→Z), title_desc (Z→A)
 * - По автору: author_asc (A→Z)
 * - По ID: id_asc (возрастание), id_desc (убывание)
 */
export type SortKey =
  | "newest" | "oldest"
  | "title_asc" | "title_desc"
  | "author_asc"
  | "id_asc" | "id_desc";

/**
 * ПРОПСЫ КОМПОНЕНТА ВЫБОРА СОРТИРОВКИ
 * 
 * @property value - Текущее выбранное значение сортировки
 * @property onChange - Функция обратного вызова при изменении выбора
 * @property className - Дополнительные CSS классы для кастомизации стилей
 */
type Props = {
  value: SortKey;
  onChange: (v: SortKey) => void;
  className?: string;
};

/**
 * КОМПОНЕНТ ВЫБОРА СПОСОБА СОРТИРОВКИ WARHAMMER 40,000 FANDOM WIKI
 * 
 * Предоставляет пользователю интерфейс для выбора способа сортировки статей:
 * - Выпадающий список с понятными вариантами на русском языке
 * - Поддержка различных критериев сортировки (дата, заголовок, автор, ID)
 * - Интеграция с родительскими компонентами через callback функцию
 */
const SortSelect: React.FC<Props> = ({ value, onChange, className }) => {
  return (
    <div className="sort-wrap">
      {/* 
        ВЫПАДАЮЩИЙ СПИСОК ВАРИАНТОВ СОРТИРОВКИ
        - value: привязка к текущему выбранному значению
        - onChange: обработчик изменения с преобразованием типа
        - className: возможность кастомизации стилей
      */}
      <select
        className={`sort-select ${className ?? ""}`}
        value={value}
        onChange={(e) => onChange(e.target.value as SortKey)}
        aria-label="Выберите способ сортировки статей"
      >
        {/* ВАРИАНТЫ СОРТИРОВКИ С ПОНЯТНЫМИ ОПИСАНИЯМИ */}
        
        {/* Сортировка по ID */}
        <option value="id_desc">ID ↓ (сначала большие)</option>
        <option value="id_asc">ID ↑ (сначала маленькие)</option>
        
        {/* Сортировка по дате создания/изменения */}
        <option value="newest">Сначала новые</option>
        <option value="oldest">Сначала старые</option>
        
        {/* Сортировка по алфавиту заголовка */}
        <option value="title_asc">Заголовок A → Z</option>
        <option value="title_desc">Заголовок Z → A</option>
        
        {/* Сортировка по автору */}
        <option value="author_asc">Автор A → Z</option>
      </select>
    </div>
  );
};

export default SortSelect;




// Комментарии объясняют:

// 1. Систему типов и пропсов

// Strict typing через TypeScript для надежности
// Все возможные варианты сортировки, поддерживаемые приложением
// Четкие интерфейсы для взаимодействия с родительскими компонентами

// 2. Функциональность компонента

// Универсальный выбор сортировки для различных типов контента
// Интуитивные описания на русском языке с указанием направления
// Гибкую интеграцию через callback функцию onChange

// 3. Пользовательский опыт

// Понятные формулировки для всех вариантов сортировки
// Визуальные индикаторы направления (стрелки, A→Z/Z→A)
// Поддержку доступности через aria-атрибуты

// 4. Архитектурные особенности

// Независимость от конкретной реализации - работает с любым родительским компонентом
// Переиспользуемость в разных частях приложения
// Легкую кастомизацию через CSS классы

// 5. Бизнес-логику сортировки

// Хронологическую сортировку (новые/старые сначала)
// Алфавитную сортировку заголовков и авторов
// Техническую сортировку по ID для отладки и администрирования

// Компонент SortSelect обеспечивает удобный и интуитивно понятный интерфейс
