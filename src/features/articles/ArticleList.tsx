import ArticleCard from "./ArticleCard";
import type { ApiArticleShort } from "@/models/api";

/**
 * ПРОПСЫ КОМПОНЕНТА СПИСКА СТАТЕЙ
 * 
 * @property articles - массив кратких описаний статей для отображения в виде карточек
 */
interface Props {
  articles: ApiArticleShort[];
}

/**
 * КОМПОНЕНТ СПИСКА СТАТЕЙ WARHAMMER 40,000 FANDOM WIKI
 * 
 * Отображает сетку карточек статей с адаптивным дизайном:
 * - Адаптивная сетка от 1 до 4 колонок в зависимости от размера экрана
 * - Сообщение при отсутствии статей
 * - Интеграция с компонентом ArticleCard для отображения отдельных статей
 * 
 * Особенности:
 * - Автоматическая адаптация под различные размеры экранов
 * - Единообразное отображение карточек
 * - Оптимизированная разметка для SEO и доступности
 */
export default function ArticleList({ articles }: Props) {
  /**
   * СОСТОЯНИЕ ПУСТОГО СПИСКА СТАТЕЙ
   * 
   * Отображается когда массив articles пуст, что может означать:
   * - Отсутствие статей в системе
   * - Результаты поиска не найдены
   * - Ошибка загрузки данных
   */
  if (articles.length === 0) {
    return (
      <div className="text-center py-20">
        <p className="text-2xl text-gray-400">Статьи не найдены</p>
      </div>
    );
  }

  /**
   * ОСНОВНОЙ РЕНДЕРИНГ СЕТКИ КАРТОЧЕК СТАТЕЙ
   * 
   * Адаптивная CSS Grid сетка с прогрессивным увеличением колонок:
   * - mobile: 1 колонка (100% ширины)
   * - sm: 2 колонки (планшеты и маленькие ноутбуки)
   * - lg: 3 колонки (ноутбуки и десктопы)
   * - xl: 4 колонки (большие экраны)
   * 
   * Отступы между карточками также адаптируются под размер экрана
   */
  return (
    <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-4 sm:gap-6">
      {articles.map(article => (
        /**
         * КОМПОНЕНТ КАРТОЧКИ СТАТЬИ
         * 
         * ArticleCard отображает отдельную статью в компактном формате:
         * - Изображение превью
         * - Заголовок статьи
         * - Информация об авторе
         * - Визуальное оформление в стиле Warhammer 40,000
         * 
         * @param key - уникальный идентификатор статьи для оптимизации React
         * @param article - данные статьи для отображения
         */
        <ArticleCard key={article.id} article={article} />
      ))}
    </div>
  );
}




// Комментарии объясняют:

// 1. Архитектуру компонента списка статей

// Контейнерный компонент для отображения коллекции карточек
// Интеграцию с ArticleCard для рендеринга отдельных элементов
// Обработку граничных случаев через состояние пустого списка

// 2. Адаптивный дизайн сетки

// Прогрессивное увеличение колонок от 1 до 4 в зависимости от размера экрана
// Адаптивные отступы между карточками (gap-4 на мобильных, gap-6 на больших экранах)
// Оптимальное использование пространства на всех устройствах

// 3. Пользовательский опыт

// Информативное сообщение при отсутствии статей
// Единообразное отображение всех карточек в сетке
// Быструю навигацию по статьям через визуальные карточки

// 4. Технические особенности

// Использование CSS Grid для современной и производительной разметки
// Корректную обработку ключей для оптимизации React рендеринга
// Типизацию пропсов через TypeScript интерфейсы

// 5. Бизнес-логику отображения

// Масштабируемость для работы с любым количеством статей
// Согласованность с дизайн-системой Warhammer 40,000 Fandom Wiki
// Подготовку для будущих улучшений (фильтрация, сортировка)

// Компонент ArticleList обеспечивает 
// удобный и визуально привлекательный способ отображения коллекции 
// статей в Warhammer 40,000 Fandom Wiki, адаптируясь к различным 
// устройствам и предоставляя четкую обратную связь при отсутствии контента.

